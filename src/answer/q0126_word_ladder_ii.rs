use std::collections::HashMap;
impl Solution {
    pub fn find_ladders(
        begin_word: String,
        end_word: String,
        word_list: Vec<String>,
    ) -> Vec<Vec<String>> {
        if !word_list.contains(&end_word) {
            return vec![];
        }
        Solution::search(
            vec![&begin_word],
            &end_word,
            &word_list,
            &mut vec![false; word_list.len()],
        )
    }

    fn search(
        begin_words: Vec<&String>,
        end_word: &String,
        word_list: &Vec<String>,
        searched_pos: &mut Vec<bool>,
    ) -> Vec<Vec<String>> {
        let mut map = HashMap::new();
        for (i, w) in word_list.iter().enumerate() {
            if searched_pos[i] {
                continue;
            }
            let mut t = vec![];
            for bw in begin_words.iter() {
                if Solution::differ_one(*bw, w) {
                    t.push(*bw);
                    searched_pos[i] = true
                }
            }
            if !t.is_empty() {
                map.insert(w.clone(), t);
            }
        }
        if map.is_empty() {
            return vec![];
        }
        if let Some(v) = map.get(end_word) {
            return v
                .iter()
                .map(|s| vec![format!("{}", s), format!("{}", end_word)])
                .collect();
        } else {
            let mut ret = vec![];
            let new_begin_words = map.keys().collect();
            let r = Solution::search(new_begin_words, end_word, word_list, searched_pos);
            for list in r.into_iter() {
                let fst = list.first().unwrap();
                let head_list = map.get(fst).unwrap();
                for nt in head_list.into_iter() {
                    let mut t: Vec<String> = vec![format!("{}", nt)];
                    t.append(&mut list.clone());
                    ret.push(t);
                }
            }
            return ret;
        }
    }

    fn differ_one(s1: &str, s2: &str) -> bool {
        let mut count = 0;
        for i in 0..s1.len() {
            if s1[i..=i] != s2[i..=i] {
                count += 1;
            }
        }
        count == 1
    }
}

